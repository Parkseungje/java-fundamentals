package com.study.part01_oop.s04_inheritance_constructor;

/**
 * [모델] 생성자 함정을 보여주기 위한 부모 클래스.
 *
 * 핵심 포인트: 이 클래스에는 '매개변수 있는 생성자'만 있고, '매개변수 없는 기본 생성자'가 없다.
 *
 * 자바는 클래스에 생성자를 하나도 안 적으면 기본 생성자 Account()를 자동으로 넣어준다.
 * 하지만 이렇게 매개변수 생성자를 직접 하나라도 정의하면, 자동 기본 생성자는 만들어지지 않는다.
 * 그 결과 Account()는 존재하지 않게 되고, 자식이 super()를 자동 호출하려 할 때 부를 대상이 없어
 * 컴파일 에러가 난다 (-> Example3에서 확인).
 *
 * [왜 매개변수 생성자를 만들면 기본 생성자를 자동 생성하지 않는가?]
 * 자동 기본 생성자는 어디까지나 '편의'다 — 생성자를 하나도 안 쓴 클래스도 객체는 만들 수 있게
 * 자바가 빈 생성자를 대신 넣어주는 것뿐이다. 그런데 개발자가 Account(String owner)처럼 생성자를
 * 직접 정의했다는 것은 "이 객체는 owner 없이는 만들면 안 된다"는 의도를 코드로 밝힌 셈이다.
 * 이때 자바가 매개변수 없는 Account()를 몰래 추가해 버리면, owner가 null인 '반쪽짜리 객체'가
 * 만들어질 수 있어 개발자가 세운 규칙(불변식, invariant)을 깨뜨린다.
 * 그래서 자바는 "개발자가 생성 방법을 직접 정했으면 그 의도를 존중하고, 편의용 기본 생성자는
 * 끼워 넣지 않는다"는 원칙을 따른다. 기본 생성자가 필요하면 개발자가 명시적으로 추가해야 한다.
 */
public class Account {

    protected String owner;

    // 매개변수 있는 생성자만 존재. 기본 생성자 Account()는 자동 생성되지 않는다.
    public Account(String owner) {
        this.owner = owner;
        System.out.println("  [Account 생성자] owner=" + owner);
    }
}
